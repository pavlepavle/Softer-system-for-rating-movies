    private void srediTabelu() {
        try {
            TblModelFilm tmf = new TblModelFilm(Kontroler.vratiInstancu().vratiListuFilmova());
            jtblFilm.setModel(tmf);
            jtblFilm.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                public void valueChanged(ListSelectionEvent e) {
                    int j = jtblFilm.getSelectedRow();
                    if (j!=-1) {
                        btnDetalji.setEnabled(true);
                        btnOceniFilm.setEnabled(true);
                    } else {
                        btnDetalji.setEnabled(false);
                        btnOceniFilm.setEnabled(false);
                    }
                }
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Poruka o gresci", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void srediFormu(){
        JSpinner.NumberEditor editor = new JSpinner.NumberEditor(jsGodina, "#"); 
        jsGodina.setEditor(editor);
    }
